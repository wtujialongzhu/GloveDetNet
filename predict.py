import time
import socket
import cv2
import numpy as np
from PIL import Image
from SendPic import runserver
from GloveDET import CAPN
import os
import threading
import _thread
global iszhengchang
iszhengchang=1
def clearhuancun():
    import os
    path = 'huancun'  # 文件路径
    if os.path.exists(path):  # 如果文件存在
        print("clear huancun")
        datanames = os.listdir(path)
        list = []
        for i in datanames:
            list.append(i)
            mydir = path + '/' + i
            print(mydir)
            try:
                os.remove(mydir)
                print("delete success")
            except Exception as e:
                print("Error: %s - %s." % (e.filename, e.strerror))
    else:
        print('no such file:%s' % path)  # 则返回文件不存在
